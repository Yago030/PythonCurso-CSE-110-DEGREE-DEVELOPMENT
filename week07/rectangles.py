
phrase = "El Perro Vuela en Cohetes"    
    
    
def display_regular(chain):
    print(f'La cadena normal es: {chain}')

def display_uppercase(chain):
    converted = chain.upper()
    print(f'La cadena mayusucala es: {converted}')
    

def display_lowercase(chain):
    converted = chain.lower()
    print(f'La cadena minuscula es: {converted}')
    
display_regular(phrase)
display_uppercase(phrase)
display_lowercase(phrase)

